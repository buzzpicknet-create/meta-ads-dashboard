    const proposedValue = String(args.new_name ?? "");
    const summary = currentValue
      ? `تغيير اسم المجموعة الإعلانية من "${currentValue}" إلى "${proposedValue}"`
      : `تغيير اسم المجموعة الإعلانية إلى "${proposedValue}"`;
